// Generate SQL INSERT statements for ALL languages
const generateSQL = () => {
	const sqlStatements = [];
	const languages = ['en', 'pt', 'es']; // Add more: 'fr', 'de', 'sv'
	
	languages.forEach(lang => {
		sqlStatements.push(`-- ============================================`);
		sqlStatements.push(`-- ${lang.toUpperCase()} TRANSLATIONS`);
		sqlStatements.push(`-- ============================================\n`);
		
		const translations = allTranslations[lang];
		
		for (const [key, value] of Object.entries(translations)) {
			// Escape single quotes in SQL
			const escapedValue = value.replace(/'/g, "''");
			
			// Extract namespace (first part before the dot)
			const namespace = key.split('.')[0];
			
			const sql = `INSERT INTO translations (key, language_code, value, namespace, is_auto_translated)
VALUES ('${key}', '${lang}', '${escapedValue}', '${namespace}', ${lang === 'en' ? 'false' : 'true'})
ON CONFLICT (key, language_code) 
DO UPDATE SET value = EXCLUDED.value, namespace = EXCLUDED.namespace, is_auto_translated = EXCLUDED.is_auto_translated, updated_at = NOW();`;
			
			sqlStatements.push(sql);
		}
		
		sqlStatements.push('\n');
	});
	
	return sqlStatements.join('\n\n');
};

